def create_blanks(word_length: int) -> list:
    display = []
    for _ in range(word_length):
        display += "_"

    return display


def guessed_already(guess: str, guessed_letters: list) -> bool:
    if guess in guessed_letters:
        return True

    guessed_letters.append(guess)


def incorrect_guess_check(guess: str, chosen_word: str) -> bool:
    if guess not in chosen_word:
        print(f"The letter '{guess}' is not in the word.")
        return True


def update_display(
    word_length: int, chosen_word: str, display: list, guess: str
) -> list:
    for position in range(word_length):
        letter = chosen_word[position]
        if letter == guess:
            display[position] = letter
    return display


def run(chosen_word: str) -> bool:
    end_of_game = False
    result = False
    already_guessed = False
    lives = 6
    guessed_letters = []

    word_length = len(chosen_word)
    display = create_blanks(word_length)

    while not end_of_game:
        guess = input("Guess a letter: ").lower()

        # Check is letter has already been guessed
        already_guessed = guessed_already(guess, guessed_letters)
        if already_guessed:
            print(f"You have already guessed the letter '{guess}'.")
            continue

        # Check if guess is incorrect
        guess_is_incorrect = incorrect_guess_check(guess, chosen_word)
        if guess_is_incorrect:
            lives -= 1
            if lives == 0:
                end_of_game = True

        # Updates and prints the display
        updated_display = update_display(word_length, chosen_word, display, guess)
        print(f"{' '.join(display)}")

        # Check if user has got all letters.
        if "_" not in updated_display:
            result = True
            end_of_game = True

        print(stages[lives])

    return result


